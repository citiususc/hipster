        // Listener to process run/pause button
        runButton.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                switch (state) {
                    case STARTED:
                        pause();
                        break;
                    case STOPPED:
                        start();
                        break;
                    case PAUSED:
                        continueExecution();
                        break;
                }
            }
        });

        resetButton.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                stop();
                loadSelectedMaze();
            }
        });

